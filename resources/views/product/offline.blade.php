    });

    $(document).on('change', '.state', function () {
        let state_id = $(this).val();
        let scities = cities.filter(function(val) {
            return val.state_id == state_id;
        });
        let html = '';
        $.each(scities, function (key, value) {
            html += '<option value="'+ value.id +'">'+ value.name +'</option>';
        });
        $('#city_id').html(html);
    });
